# -*- coding: utf-8 -*-
"""Детерминированные гварды до-извлечения для журнальных доков (kg_value=2).

Два уровня:
  1. preprocess_text(text)  — построчный препроцесс ДО склейки/сегментации:
     вырезает строки библиографического аппарата (DOI/УДК/ISSN/ГРНТИ/©/URL/e-mail).
  2. is_noise_sentence(sent) -> (bool, reason) — sentence-level фильтр ПОСЛЕ
     SENT.split, ДО parse_values: рыночная статистика, многоколоночный гарбл,
     нулевая плотность глаголов.
Без LLM, без состояния — чистые функции, полностью воспроизводимо.
"""
import re

# ---------------------------------------------------------------- 1. препроцесс
# Строка убивается целиком, если содержит маркер библиографического аппарата.
_LINE_KILL = re.compile(
    r"(?:\bУДК\b|\bГРНТИ\b|\bISSN\b|\bISBN\b|\bDOI\b|doi[.:]|dx\.doi|"
    r"https?://|www\.|©|\beLIBRARY\b|\bEDN\b|e-?mail|@[a-z0-9.-]+\.[a-z]{2,})",
    re.I)
# Колонтитул журнала: «"Цветные металлы". 2013. № 4» и т.п.
_RUNNING_HEAD = re.compile(r"[»\"]\s*\.?\s*20\d\d\.\s*№\s*\d+")
# Библиографическая ссылка: «... С. 15–23.» / «Т. 5. № 2.» / «Vol. 12, No. 3»
_BIBLIO = re.compile(r"(?:\bС\.\s*\d+\s*[-–—]\s*\d+|\bТ\.\s*\d+\.\s*№|\bVol\.\s*\d+|\bNo\.\s*\d+,?\s*[Pp]\.)")

def preprocess_text(text: str) -> str:
    """Вырезает мусорные строки; структура строк сохраняется (пустая замена)."""
    out = []
    for ln in text.split("\n"):
        if _LINE_KILL.search(ln) or _RUNNING_HEAD.search(ln) or _BIBLIO.search(ln):
            out.append("")          # держим позицию строки — сегментация не едет
        else:
            out.append(ln)
    return "\n".join(out)

# ------------------------------------------------------- 2а. рыночный фильтр
# «+10,1 % к 2011 г.» — сравнение с прошлым годом, ядро рыночной статистики.
_YOY = re.compile(r"%\s*(?:\)?\s*)?(?:к|против|относительно)\s+(?:19|20)\d\d\s*г")
# Денежные объёмы: млрд/млн/тыс. долл|руб|евро, долл/т, центов за фунт
_MONEY = re.compile(
    r"(?:млрд|млн|тыс)\s*\.?\s*(?:долл|руб|евро|USD|дол)|долл(?:аров)?\.?\s*(?:США)?\s*/\s*т\b|"
    r"центов?\s+за\s+фунт|долл\.?\s*/\s*унц", re.I)
# Рыночная лексика (основы слов)
_MARKET_STEM = re.compile(
    r"(?:рынк|рыноч|цен(?:а|ы|ов|е)\b|продаж|экспорт|импорт|прогноз|квартал|выручк|"
    r"спрос|потреблени|поставк|котиров|биржев|\bLME\b|производств[оа]\s+в\s+мире|"
    r"мирово[ег]о?\s+производств)", re.I)
# %-изменение при глаголах динамики
_GROWTH_PCT = re.compile(
    r"(?:рост|вырос|вырастет|снизил|снизится|снижени|увеличил|увеличится|увеличени|"
    r"сократил|сократится|сокращени|упал|упадет|падени|состав(?:ил|ит)|"
    r"прирост|подорожа|подешеве)\w*.{0,32}?\d[\d\s.,]*\s*(?:%|млн\s*т|тыс\.\s*т)|"
    r"\d[\d\s.,]*\s*%.{0,24}?(?:рост|снижени|увеличени|сокращени|прирост)", re.I | re.S)

def is_market(sent: str) -> bool:
    if _YOY.search(sent):
        return True
    hits = int(bool(_MONEY.search(sent))) + int(bool(_GROWTH_PCT.search(sent)))
    return bool(_MARKET_STEM.search(sent)) and hits >= 1

# ------------------------------------------------------- 2б. гарбл-детектор
_NUM_UNIT_RUN = re.compile(
    r"(?:[-+±<>~]?\d[\d\s.,]{0,12}\s*(?:[°о]\s*[CС]\b|%|МПа|кПа|ГПа|мм|см|мкм|нм|км|кг|мг|т\b|"
    r"г/[а-яa-z³0-9]+|кВт|МВт|ГВт|м3|м³|м2|м²|мин\b|ч\b|об/мин|А\b|В\b|Гц)\s*){3,}")
_WORD = re.compile(r"[А-Яа-яЁёA-Za-z]{3,}")
_CONNECT = re.compile(  # связки/предлоги/глаголы между числами — признак живого текста
    r"\b(и|или|от|до|при|для|с|по|на|в|составля|равн|достига)\b", re.I)
# Колонка OCR-гарбла: >=3 коротких строк «число [ед.]» внутри предложения
_COL_LINE = re.compile(r"^\s*[-+±]?\d[\d.,]{0,8}\s*(?:[°о]\s*[CС]|%|МПа|мм|см|м|кг|т|ч|мин)?\s*$")

def _col_lines(sent: str) -> int:
    return sum(1 for ln in sent.split("\n") if _COL_LINE.match(ln))

def is_garble(sent: str) -> bool:
    # (1) >=3 подряд число+единица без связующих слов между ними
    m = _NUM_UNIT_RUN.search(sent)
    if m and not _CONNECT.search(m.group(0)):
        return True
    # (2) колонка цифр по строкам (многоколоночная вёрстка PDF)
    if _col_lines(sent) >= 3:
        return True
    # (3) доля цифр+пунктуации-разделителей > 40% символов
    # (кроме телеграфной записи условий опыта: «расход … 18 кг/т; рН = 9,8–11,0»)
    ndig = sum(1 for c in sent if c.isdigit() or c in ".,;:%–—-/()°")
    if len(sent) >= 40 and ndig / len(sent) > 0.40 and not _METRIC_NOUN.search(sent):
        return True
    # (4) подпись рисунка: >=3 нумерованных позиций «(1)…(2)…(3)» или «(а)…(б)»
    if len(re.findall(r"\(\s*[0-9а-жa-g]\s*\)", sent)) >= 3:
        return True
    return False

# ------------------------------------------------- 2в. плотность глаголов ~0
_VERB = re.compile(
    r"[А-Яа-яЁё]{2,}(?:ет|ют|ит|ат|ят|ется|ится|ются|атся|ется|лся|лась|лось|лись|"
    r"ли|ла|ло|ен[аоы]?|ан[аоы]?)\b|"
    r"\b(?:составля\w+|содерж\w+|равн\w+|достига\w+|показа\w+|получ\w+|приведен\w*|"
    r"использ\w+|применя\w+|снижа\w+|повыша\w+|обеспечива\w+|является|имеет|был[аио]?|"
    r"будет|может|следует|наблюда\w+)\b")

# Телеграфный, но валидный факт: «Co – 93-94%», «напряжение – 40 кВ», «ST0 – растворимость»
_DASH_BIND = re.compile(r"[A-Za-zА-Яа-яЁё0-9)][\s]*[–—-]\s*[<>~≤≥±]?\d")
# Метрические существительные — предложение фактоносное даже без глагола.
# НАМЕРЕННО регистрозависимо (lowercase): «СОДЕРЖАНИЕ» (шапка оглавления) не спасает.
_METRIC_NOUN = re.compile(
    r"одержан|емператур|давлени|асход|онцентрац|звлечен|лотност|корост|"
    r"рочност|лажност|рупност|тепен[ьи]|апряжени|ыход\b|лубин|\bрН\b|\bpH\b")
_VERB_EN = re.compile(r"\b(?:is|are|was|were|has|have|be|been)\b|[a-z]{3,}ed\b")
# «8-10 г/дм3 Fe;» — химсимвол при числе = телеграфный факт состава
_ELEMS = r"(?:Fe|Ni|Cu|Co|Au|Ag|Pt|Pd|Zn|Pb|Al|Mg|Si|Ca|Ti|Cr|Mn|As|Se|Te|Sb|Bi|Sn|Cd|Mo|W|V|S)"
_ELEM_NUM = re.compile(
    r"\d\s*(?:%|г/[а-яa-z³0-9]+|ppm)?\s*\b" + _ELEMS + r"\b|"
    r"\b" + _ELEMS + r"\b[\s(–—:-]{0,3}\d")

def verb_density_zero(sent: str) -> bool:
    # телеграфная запись факта («Co – 93-94%», «Температура раствора - 80оС»)
    # валидна для грамматики — НЕ шум, даже без глагола
    if _DASH_BIND.search(sent) or _METRIC_NOUN.search(sent) or _ELEM_NUM.search(sent):
        return False
    if _VERB.search(sent) or _VERB_EN.search(sent):
        return False            # глагол есть — живой текст
    words = _WORD.findall(sent)
    return len(words) < 6       # без глагола и почти без слов — колонка/шапка

# ---------------------------------------------------------------- фасад
def is_noise_sentence(sent: str):
    """(True, причина) если предложение нельзя отдавать в parse_values."""
    if is_market(sent):
        return True, "market"
    if is_garble(sent):
        return True, "garble"
    if verb_density_zero(sent):
        return True, "no_verb"
    return False, None
